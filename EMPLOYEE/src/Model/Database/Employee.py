# Create an employee class

class Employee:

    # Initialize the attribute
    def __init__(self, employeeID, gender, age, sales, bmi, salary, birthdate):
        self.employeeID = employeeID
        self.gender = gender
        self.age = age
        self.sales = sales
        self.bmi = bmi
        self.salary= salary
        self.birthdate = birthdate

